     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'name',
            'categoryId',
            'imgUrl',
            'quantity',
            'priceSale',
            'description',
            'ramId',
            'romId',
            'colorId',
            'sim',
            'screen',
            'camera',
            'ratingStart',
            'viewCustomer',
            'trash'
        ]);

        $phone = Phone::create($request->all());

        return [
            "status" => 1,
            "data" => $phone,

        ];
    }

